// Описание переменных для списка
// std::vector<int> a;  // Динамический массив (список), поддерживает добавление/удаление.
// Требует явного подключения библиотеки <vector> и управления памятью.

#include <iostream>
#include <vector>

int main() {
    std::vector<int> a;  // Пустой список (вектор)

    // Добавление элементов
    a.push_back(1);  // Добавляем 1
    a.push_back(2);  // Добавляем 2
    a.push_back(3);  // Добавляем 3

    // Вывод списка
    std::cout << "Содержимое списка: ";
    for (const auto& elem : a) {
        std::cout << elem << " ";
    }
    std::cout << std::endl;  // Вывод: 1 2 3

    // Дополнительные операции
    std::cout << "Первый элемент: " << a[0] << std::endl;  // Первый элемент: 1
    std::cout << "Размер: " << a.size() << std::endl;  // Размер: 3

    return 0;
}
