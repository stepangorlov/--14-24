# Формулируем стек на новую задачу: хештег «создание стека»
# m = []  # Пустой список, используемый как стек (LIFO - последний вошел, первый вышел).
# Стек поддерживает операции append() для добавления и pop() для удаления с конца.

m = []  # Инициализация пустого стека

# Push операции (добавление)
m.append(10)  # Push 10
m.append(20)  # Push 20
m.append(30)  # Push 30

print("Стек после добавления:", m)  # Вывод: [10, 20, 30]

# Pop операция (удаление)
popped = m.pop()  # Удаляем 30
print("Удаленный элемент:", popped)  # Вывод: 30
print("Стек после удаления:", m)  # Вывод: [10, 20]

# Проверка пустоты
if not m:
    print("Стек пуст")
else:
    print("Верхний элемент:", m[-1])  # Верх: 20
