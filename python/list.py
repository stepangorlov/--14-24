# Описание переменных для списка
# a = []  # Это пустой список, динамическая структура данных, аналог массива.
# Список в Python поддерживает добавление (append), удаление (pop), доступ по индексу и срезы.

a = []  # Инициализация пустого списка

# Добавление элементов в список
a.append(1)  # Добавляем 1
a.append(2)  # Добавляем 2
a.append(3)  # Добавляем 3

# Вывод списка
print("Содержимое списка:", a)  # Вывод: [1, 2, 3]

# Дополнительные операции
print("Первый элемент:", a[0])  # Первый элемент: 1
print("Длина списка:", len(a))  # Длина: 3
